package com.omar.myapps.swipped_items_forrecyclerview;

public class Car {

    private String name;
    private int model;

    public Car(String name, int model) {
        this.name = name;
        this.model = model;
    }

    public String getName() {
        return name;
    }

    public int getModel() {
        return model;
    }
}
